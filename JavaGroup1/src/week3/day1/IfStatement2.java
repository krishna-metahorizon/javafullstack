package week3.day1;

public class IfStatement2 {

	public static void main(String[] args) {
		int firstNumber=8; //input from user
		if(firstNumber==0) //Equals to
			System.out.println("Zero");
		if(firstNumber!=0) //Not Equals to
			System.out.println("Non-Zero");
		if(!(firstNumber==0)) //Not Equals to
			System.out.println("Non-Zero");
	}
}
